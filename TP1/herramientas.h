#ifndef HERRAMIENTAS
#define HERRAMIENTAS

/** \brief  Suma dos enteros ingresados
 *
 * \param   Primer entero a sumar
 * \param   Segundo entero a sumar
 * \return  Retorna el resultado de la suma
 *
 */

int sumar(int a, int b);

/** \brief Resta dos enteros ingresados
 *
 * \param   Primer entero a restar
 * \param   Segundo entero a restar
 * \return  Retorna el resultado de la resta
 *
 */

int restar(int a, int b);

/** \brief Multiplica dos enteros ingresados
 *
 * \param   Primer entero a multiplicar
 * \param   Segundo entero a multiplicar
 * \return  Retorna el resultado de la multiplicacion
 *
 */
int multiplicar(int a, int b);

/** \brief Divide dos enteros ingresados
 *
 * \param   Dividendo de la division (int)
 * \param   Divisor de la division (int)
 * \return  Retorna el resultado de la division como float
 *
 */
float dividir(int a, int b);

/** \brief Calcula el factorial de un entero
 *
 * \param   Entero a factorizar
 * \return  Retorna el resultado de la factorizacion
 *
 */
double factorizar(int a);

int getIntPositivo();


#endif // HERRAMIENTAS
